// Relational or comparison operators

// > - more than
// < - less than
// >= - more than or equal
// <= - less than or equal

var result = 10 > 5;
console.log(result);
var result = 10 < 5;
console.log(result);
var result = 10 <= 10;
console.log(result);
var result = 11 >= 3;
console.log(result);

//equality operators
